CREATE TABLE IF NOT EXISTS RAW.GAMES (
    GAME_ID NUMBER,
    GAME_DATE DATE,
    SEASON NUMBER,
    GAME_TYPE STRING,
    STATUS STRING,
    HOME_TEAM_ID NUMBER,
    AWAY_TEAM_ID NUMBER,
    HOME_SCORE NUMBER,
    AWAY_SCORE NUMBER,
    WINNING_TEAM_ID NUMBER,
    LOSING_TEAM_ID NUMBER,
    VENUE_NAME STRING,
    DAY_NIGHT STRING,
    CREATED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

